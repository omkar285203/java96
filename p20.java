//20.WAP to calculate Square roots of 1 to 9 numbers
public class p20 {
    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++) {
            double squareRoot = Math.sqrt(i);
            System.out.println("Square root of " + i + " is: " + squareRoot);
        }
    }
}
